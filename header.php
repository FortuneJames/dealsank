      var tagManager = {};
      var buses = {
        launch: {
          isPresent: !!window._satellite && !!window._satellite.company,
          trigger: function(ruleName, eventDetail) {
            window._satellite.track(ruleName, eventDetail);
          },
          get: function(varName) {
            return window._satellite.getVar(varName);
          }
        },
